public class Operation2 {
    public static void main(String[] args) {
        //关系运算符
        int a = 10;
        int b = 5;
        System.out.println( a > b );
        System.out.println( a >= b );
        System.out.println( a < b );
        System.out.println( a <= b );
        System.out.println( a != b );
        System.out.println( a == b );
    }
}
